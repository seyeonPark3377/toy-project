package address.model;

public class AddressDTO {
	private int addressId;
	private String addressOwner;
	private String addressName;
	private String addressPhone;
	private String addressHome;
	private String addressEmail;
	private String addressGroup;
	private String addressMemo;
	private int addressBookmark;
	
	public int getAddressId() {
		return addressId;
	}
	public void setAddressId(int addressId) {
		this.addressId = addressId;
	}
	public String getAddressOwner() {
		return addressOwner;
	}
	public void setAddressOwner(String addressOwner) {
		this.addressOwner = addressOwner;
	}
	public String getAddressName() {
		return addressName;
	}
	public void setAddressName(String addressName) {
		this.addressName = addressName;
	}
	public String getAddressPhone() {
		return addressPhone;
	}
	public void setAddressPhone(String addressPhone) {
		this.addressPhone = addressPhone;
	}
	public String getAddressHome() {
		return addressHome;
	}
	public void setAddressHome(String addressHome) {
		this.addressHome = addressHome;
	}
	public String getAddressEmail() {
		return addressEmail;
	}
	public void setAddressEmail(String addressEmail) {
		this.addressEmail = addressEmail;
	}
	public String getAddressGroup() {
		return addressGroup;
	}
	public void setAddressGroup(String addressGroup) {
		this.addressGroup = addressGroup;
	}
	public String getAddressMemo() {
		return addressMemo;
	}
	public void setAddressMemo(String addressMemo) {
		this.addressMemo = addressMemo;
	}
	public int getAddressBookmark() {
		return addressBookmark;
	}
	public void setAddressBookmark(int addressBookmark) {
		this.addressBookmark = addressBookmark;
	}

}